int		ft_count_num_amount(int argc, char **argv)
{
	int i;
	int j;
	int num;

	argc = 0;
	num = 0;
	i = 1;
	while (i < 10)
	{
		j = 0;
		while (j < 9)
		{
			if (argv[i][j] >= '1' && argv[i][j] <= '9')
			{
				num++;
			}
			j++;
		}
		i++;
	}
	return (num);
}
