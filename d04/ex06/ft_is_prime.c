int	ft_is_prime(int nb)
{
	int	i;
	int	k;

	k = 0;
	i = 1;
	while (i++ <= nb)
	{
		if (nb <= 3 && nb > 1)
		{
			k = 1;
			break ;
		}
		if ((((nb % i) == 0) && (i != nb)))
		{
			k = 0;
			break ;
		}
		k = 1;
	}
	if (k)
		return (1);
	else
		return (0);
}
