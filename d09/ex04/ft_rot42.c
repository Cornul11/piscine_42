char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z')
				|| (str[i] >= 'a' && str[i] <= 'z'))
		{
			if (str[i] + 16 <= 'Z' || str[i] + 16 <= 'z')
				str[i] = str[i] + 16;
			else if (str[i] + 16 >= 'Z' || str[i] + 16 >= 'z')
				str[i] = str[i] - 10;
		}
		i++;
	}
	return (str);
}
